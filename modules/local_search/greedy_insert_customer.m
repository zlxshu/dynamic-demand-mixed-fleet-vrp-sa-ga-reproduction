function [routesBest, costBest, ok] = greedy_insert_customer(routes, cust, srcIdx, costNow, n, K, G, ~)
% =========================================================================
% [???] greedy_insert_customer
%  ????: ???????:????????????°§???????¶À??(??????????)
%  ??????: ????
%  ???: ??˜ˆ?∑⁄,???? G ???????? global.
% =========================================================================
% ?? cust ?????? srcIdx ???????°§????(????ùﬁ CV),????????ß”????
ok = false;
routesBest = routes;
costBest   = costNow;

% ???°§?????????:CV -> EV(???? srcIdx)
order = [setdiff(1:G.nCV, srcIdx, 'stable'), setdiff(G.nCV+1:K, srcIdx, 'stable')];

for t = order
    L = numel(routes{t});
    posList = unique([0, floor(L/2), L]); % ????????¶À??,????
    for p = posList
        candRoutes = routes;
        if p == 0
            candRoutes{t} = [cust, routes{t}];
        elseif p >= L
            candRoutes{t} = [routes{t}, cust];
        else
            candRoutes{t} = [routes{t}(1:p), cust, routes{t}(p+1:end)];
        end

        [perm2, cuts2] = merge_routes_to_perm_pub(candRoutes, n, K);
        candCh = [perm2, cuts2];

        [f2, fe2, candFixed] = fitness_strict_penalty(candCh, G);
        if fe2 && (f2 < costBest - 1e-6)
            % ????:??? candFixed ?????????????(?????????)
            permF = candFixed(1:n);
            cutsF = sort(candFixed(n+1:n+K-1));
            routesBest = split_perm_by_cuts_pub(permF, cutsF, n, K);
            costBest   = f2;
            ok = true;
        end
    end
end
end
