function [bestCh, bestCost] = post_improve_best(ch0, cost0, G)
% =========================================================================
% [???] post_improve_best
%  ????: ?????????????????????(???????????)
%  ???: ??????? 2-opt + CV/EV ???? + EV->CV relocate
% =========================================================================

n = G.n; K = G.K;
bestCh = ch0;
bestCost = cost0;

% ?????????????,??????(????"¯¯???")
[~, fe0] = fitness_strict_penalty(bestCh, G);
if ~fe0
    return;
end

rs = RandStream('mt19937ar','Seed', 20260106);

% 1) ???????????? 2-opt(???????,??????)
bestCh = two_opt_within_routes_rs(bestCh, n, K, 200, G, rs);
[bestCost, fe] = fitness_strict_penalty(bestCh, G);
if ~fe
    bestCh = ch0;
    bestCost = cost0;
    return;
end

% 2) ???? CV/EV ????(?????? EV ?????????"??????")
if isfield(G,'nEV') && G.nEV>0 && isfield(G,'nCV') && G.nCV>0
    for it = 1:120
        candCh = swap_one_between_cv_ev(bestCh, n, K, G, rs);
        [c2, fe2, candFixed, ~] = fitness_strict_penalty(candCh, G);
        if fe2 && c2 + 1e-9 < bestCost
            bestCost = c2;
            bestCh = candFixed;
        end
    end
end

% 2.5) EV->CV relocate:?????"??EV¯¯??"???
if isfield(G,'nEV') && G.nEV>0 && isfield(G,'nCV') && G.nCV>0
    [bestCh, bestCost] = relocate_longest_ev_to_cv(bestCh, bestCost, n, K, G, rs, 200);
end
end
