function tf = cv_only_case(G)
% =========================================================================
% [模块] cv_only_case
%  功能: 判断是否为 CV-only 场景(无EV).
%  论文对应: 实现层
%  说明: 模块化版本.
% =========================================================================
tf = isfield(G,'nEV') && isfield(G,'nCV') && (G.nEV == 0) && (G.nCV > 0);
end
