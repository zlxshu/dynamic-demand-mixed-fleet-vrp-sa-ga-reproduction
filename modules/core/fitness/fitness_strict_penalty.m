function [fx, feasible, ch_fixed, detail] = fitness_strict_penalty(ch, G)
% fitness_strict_penalty - ???????? + ?????
% ??:
%   ch - ??? [1 x (n+K-1)]
%   G  - ????????? n,K,D,q,Qmax,Speed,B0,Bchg,gE,LT,RT,ST ??
% ??:
%   fx        - ??????=????????=?????
%   feasible  - ?????
%   ch_fixed  - ???????
%   detail    - ????
%
% === ?? 4.2.3 ???????????paper_repro ?????===
% ?????F(x) = 1/f(x)??? f(x) ???????????
%           ? f(x)>0?? F(x)=1/f(x)?? f(x)=0?? F(x)=0?
%           F(x) ??? [0,1]???????????
%
% ?????????? fx = f(x)??????? F(x)??????
%           ??????? selection_weight_penalty ??? w = 1/f ???????
%           ????????? F(x)=1/f(x) ????????????????
%
% ????????????????? P_BIG + P_BAD*(1+vioScore)?
%         ?? inf???? Metropolis ?? exp(-dF/T) ???????????
%         ????????????????????????
% ============================================

    P_BIG = 1e9;
    P_BAD = 1e7;  % 有限大罚

    [ch_fixed, ok, detail, vio] = decode_core_return_fixed_with_vio(ch, true, G);

    if ok
        fx = sum([detail.totalCost]);
        feasible = true;
    else
        % 分层罚:不可达(最大) > 时间窗 > 容量 > 电量边界 > 充电次数约束
        w_unreach = 10.0;
        w_tw      = 3.0;
        w_cap     = 2.0;
        w_bat     = 1.5;
        w_charge  = 1.0;

        vioScore = w_unreach*vio.unreach + w_tw*vio.tw + w_cap*vio.cap + w_bat*vio.bat + w_charge*vio.charge;

        % 罚函数:基础大罚 + 违规量加权(保证非inf;且不同违规有层次)
        fx = P_BIG + P_BAD * (1 + vioScore);
        feasible = false;
    end
end
